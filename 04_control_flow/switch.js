const month=3
switch (month) {                        // We can use Switch to compare one value with number if different values.
    case 1:
        console.log('January')
        break;
    case 2:
        console.log('Feb')
        break;
    case 3:
        console.log('March')
        break;
    case 4:
        console.log('April')
        break;

    default:
        console.log('default case match')
        break;
}

